    private <T> String concatenarArray(T[] array) {
        StringBuilder result = new StringBuilder();

        for (int i = 0; i < array.length; i++) {
            result.append(array[i]);

            if (i < array.length - 1) {
                result.append(";"); // Adiciona ponto e vírgula entre os valores
            }
        }

        return result.toString();
    }



    private void enviarRequisicao(Object[] array) {
        String mensagem = concatenarArray(array);
        String resposta = cliente.enviaMensagem(mensagem);
        txtResposta.setText(resposta);
    }
    

    
                
                
